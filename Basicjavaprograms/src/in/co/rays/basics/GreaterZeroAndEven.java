package in.co.rays.basics;

public class GreaterZeroAndEven 
{
public static void main(String[] args) 
{
	int checknum=3;
	
	if(checknum>=0 && checknum%2==0)
	{
		System.out.println("The number is greater than 0 and even");
	}
	else
	{
		System.out.println("The number is neither 0 nor even");
	}
}
}
